// Write code to find average as mentioned above. Use array and object methods.

const students = {
    student1: {
        subject1: 44,
        subject2: 56,
        subject3: 87,
        subject4: 97,
        subject5: 37
    },
    student2: {
        subject1: 44,
        subject2: 56,
        subject3: 87,
        subject4: 97,
        subject5: 37
    },
    student3: {
        subject1: 44,
        subject2: 56,
        subject3: 87,
        subject4: 97,
        subject5: 37
    }
};

const averageScores = Object.keys(students).map(studentKey => {
    const student = students[studentKey];
    const totalScore = Object.values(student).reduce((sum, score) => sum + score, 0);
    const average = totalScore / Object.values(student).length;
    return {
        [studentKey]: { average: average.toFixed(2) } };
});

console.log(averageScores);